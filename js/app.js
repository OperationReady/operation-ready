function getProgress() {
  try {
    return JSON.parse(localStorage.getItem('or_progress') || '{}');
  } catch(e) { return {}; }
}

function markModuleComplete(moduleId, score, total) {
  const p = getProgress();
  p[moduleId] = {
    completed: true,
    score: score || 0,
    total: total || 0,
    date: new Date().toISOString().slice(0, 10)
  };
  localStorage.setItem('or_progress', JSON.stringify(p));
}

function isModuleComplete(moduleId) {
  const p = getProgress();
  return !!(p[moduleId] && p[moduleId].completed);
}

function getTrackProgress(trackNum) {
  const modules = TRACK_MODULES[trackNum];
  if (!modules) return { completed: 0, total: 0, percent: 0 };
  const p = getProgress();
  const completed = modules.filter(m => p[m] && p[m].completed).length;
  return {
    completed,
    total: modules.length,
    percent: Math.round((completed / modules.length) * 100)
  };
}

function getOverallProgress() {
  const p = getProgress();
  const completed = ALL_MODULES.filter(m => p[m] && p[m].completed).length;
  return {
    completed,
    total: ALL_MODULES.length,
    percent: Math.round((completed / ALL_MODULES.length) * 100)
  };
}

function getNextIncompleteModule() {
  const p = getProgress();
  for (const m of ALL_MODULES) {
    if (!p[m] || !p[m].completed) return m;
  }
  return null; // all complete
}

function resetProgress() {
  if (confirm('Reset all progress? This cannot be undone.')) {
    localStorage.removeItem('or_progress');
    location.reload();
  }
}

// Auto-detect module completion by watching for the results block to appear.
// This runs on every module page — non-invasive, doesn't need each module edited.
function initModuleProgressHook() {
  const match = location.pathname.match(/\/modules\/(\d-\d)\.html/);
  if (!match) return;
  const moduleId = match[1];

  // Watch for the quiz result to be rendered
  const observer = new MutationObserver(() => {
    // Standard modules: #quiz-result gets populated after quiz completes
    const qr = document.getElementById('quiz-result');
    if (qr && qr.innerHTML.trim() !== '' && !qr.dataset.recorded) {
      const serifEls = qr.querySelectorAll('[style*="DM Serif"]');
      let score = 0, total = 0;
      for (const el of serifEls) {
        const txt = el.textContent.trim();
        const m = txt.match(/^(\d+)\s*\/\s*(\d+)$/);
        if (m) { score = parseInt(m[1]); total = parseInt(m[2]); break; }
      }
      if (total > 0) {
        markModuleComplete(moduleId, score, total);
        qr.dataset.recorded = '1';
      }
    }
    // Simulator module (2-6): #final-score is populated with "N/N"
    const fs = document.getElementById('final-score');
    if (fs && fs.textContent.trim() !== '' && !fs.dataset.recorded) {
      const m = fs.textContent.trim().match(/^(\d+)\s*\/\s*(\d+)$/);
      if (m) {
        markModuleComplete(moduleId, parseInt(m[1]), parseInt(m[2]));
        fs.dataset.recorded = '1';
      }
    }
  });
  observer.observe(document.body, { childList: true, subtree: true, characterData: true });
}


function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('or_lang', lang);
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  const t = translations[lang];
  if(!t) return;
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if(t[key] !== undefined) el.innerHTML = t[key];
  });
}

