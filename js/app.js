function surveyRate(btn) {
  const group = btn.parentElement;
  group.querySelectorAll('.survey-star').forEach(b => b.classList.remove('selected'));
  btn.classList.add('selected');
  group.dataset.value = btn.dataset.val;
}

function surveySend(pathNum, pathTitle) {
  const wrap = document.getElementById(`survey-wrap-${pathNum}`);
  const ratingGroup = wrap.querySelector('.survey-rating');
  const rating = ratingGroup.dataset.value || '(not answered)';
  const q2 = document.getElementById(`survey-q2-${pathNum}`).value.trim() || '(not answered)';
  const q3 = document.getElementById(`survey-q3-${pathNum}`).value.trim() || '(not answered)';
  const q4 = document.getElementById(`survey-q4-${pathNum}`).value.trim() || '(not provided)';

  const subject = `Operation Ready feedback — Path ${pathNum}: ${pathTitle}`;
  const body = `Path ${pathNum} — ${pathTitle}
Submitted: ${new Date().toLocaleDateString('en-GB', {day:'numeric', month:'long', year:'numeric'})}

1. Usefulness rating (1-5): ${rating}

2. Anything unclear, missing, or out of step with operational reality?
${q2}

3. What would have helped you most before your first deployment?
${q3}

4. Role and organisation:
${q4}

---
Sent via operationready.org`;

  const mailto = `mailto:contact@operationready.org?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailto;

  // Confirmation state
  setTimeout(() => {
    wrap.innerHTML = `
      <div class="survey-eyebrow">Thank you</div>
      <div class="survey-title">Your email client should have opened</div>
      <p class="survey-intro">If nothing happened, your device may not have an email client configured. You can email the same feedback directly to <a href="mailto:contact@operationready.org" style="color:var(--teal);">contact@operationready.org</a>.</p>
    `;
  }, 600);
}

